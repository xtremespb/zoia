import utils from "../../../shared/lib/utils";
import columnsSaveData from "./data/columnsLoad.json";

export default () => ({
    schema: {
        body: columnsSaveData.root
    },
    attachValidation: true,
    async handler(req) {
        const {
            log,
            response,
            auth,
        } = req.zoia;
        // Check permissions
        if (!auth.statusAdmin()) {
            response.unauthorizedError();
            return;
        }
        // Validate form
        if (req.validationError) {
            log.error(null, req.validationError ? req.validationError.message : "Request Error");
            response.validationError(req.validationError || {});
            return;
        }
        try {
            const columns = await utils.getTableSettings(req, this.mongo.db, auth, req.body.table);
            response.successJSON({
                columns
            });
            return;
        } catch (e) {
            log.error(e);
            // eslint-disable-next-line consistent-return
            return Promise.reject(e);
        }
    }
});
